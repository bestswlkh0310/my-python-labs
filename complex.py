x = 1 + 4j  # (1+4j)
x = -2j  # (-0-2j)
x = complex(1, 2)  # (1+2j)

# 허수 부분
print(x.imag)  # 2.0

# 실수 부분
print(x.real)  # 1.0

print(1j * 1j)  # (-1+0j)
print(1 / (3 - 2j))  # (0.23076923076923078+0.15384615384615385j)

print(2 ** 1j)  # (0.7692389013639721+0.6389612763136348j)
print(1j ** 1j)  # (0.20787957635076193+0j)
